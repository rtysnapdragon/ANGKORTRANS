DELIMITER //

CREATE TRIGGER TRG_SALES_ORDER_LINE_AFTER_INSERT
AFTER INSERT ON SALES_ORDER_LINE
FOR EACH ROW
BEGIN
    UPDATE SALES_ORDER 
    SET SUBTOTAL = (SELECT SUM(LINE_TOTAL) FROM SALES_ORDER_LINE WHERE SALES_ORDER_ID = NEW.SALES_ORDER_ID),
        TOTAL_AMOUNT = SUBTOTAL + TAX_AMOUNT + SHIPPING_COST
    WHERE SALES_ORDER_ID = NEW.SALES_ORDER_ID;
END //

CREATE TRIGGER TRG_PURCHASE_ORDER_LINE_AFTER_INSERT
AFTER INSERT ON PURCHASE_ORDER_LINE
FOR EACH ROW
BEGIN
    UPDATE PURCHASE_ORDER 
    SET SUBTOTAL = (SELECT SUM(LINE_TOTAL) FROM PURCHASE_ORDER_LINE WHERE PURCHASE_ORDER_ID = NEW.PURCHASE_ORDER_ID),
        TOTAL_AMOUNT = SUBTOTAL + TAX_AMOUNT + SHIPPING_COST
    WHERE PURCHASE_ORDER_ID = NEW.PURCHASE_ORDER_ID;
END //

CREATE TRIGGER TRG_GOODS_RECEIPT_LINE_AFTER_INSERT
AFTER INSERT ON GOODS_RECEIPT_LINE
FOR EACH ROW
BEGIN
    -- Update inventory
    INSERT INTO INVENTORY (PRODUCT_ID, WAREHOUSE_ID, LOCATION_ID, QUANTITY_ON_HAND)
    VALUES (NEW.PRODUCT_ID, 
            (SELECT WAREHOUSE_ID FROM GOODS_RECEIPT WHERE RECEIPT_ID = NEW.RECEIPT_ID),
            NEW.LOCATION_ID,
            NEW.QUANTITY_ACCEPTED)
    ON DUPLICATE KEY UPDATE 
        QUANTITY_ON_HAND = QUANTITY_ON_HAND + NEW.QUANTITY_ACCEPTED,
        UPDATED_AT = CURRENT_TIMESTAMP;

    -- Log stock movement
    INSERT INTO STOCK_MOVEMENT (PRODUCT_ID, WAREHOUSE_ID, LOCATION_ID, MOVEMENT_TYPE, 
                                QUANTITY, REFERENCE_ID, REFERENCE_TYPE, NOTES)
    VALUES (NEW.PRODUCT_ID,
            (SELECT WAREHOUSE_ID FROM GOODS_RECEIPT WHERE RECEIPT_ID = NEW.RECEIPT_ID),
            NEW.LOCATION_ID,
            'RECEIPT',
            NEW.QUANTITY_ACCEPTED,
            NEW.RECEIPT_ID,
            'GOODS_RECEIPT',
            CONCAT('Receipt: ', NEW.RECEIPT_ID));
END //

