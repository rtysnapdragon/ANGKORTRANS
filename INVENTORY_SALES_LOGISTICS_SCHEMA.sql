CREATE VIEW V_INVENTORY_STATUS AS
SELECT 
    P.PRODUCT_ID,
    P.SKU,
    P.PRODUCT_NAME,
    C.CATEGORY_NAME,
    S.SUPPLIER_NAME,
    W.WAREHOUSE_NAME,
    L.LOCATION_CODE,
    I.QUANTITY_ON_HAND,
    I.QUANTITY_RESERVED,
    I.QUANTITY_AVAILABLE,
    I.REORDER_LEVEL,
    CASE 
        WHEN I.QUANTITY_AVAILABLE <= I.REORDER_LEVEL THEN 'REORDER'
        WHEN I.QUANTITY_AVAILABLE = 0 THEN 'OUT_OF_STOCK'
        ELSE 'OK'
    END AS STOCK_STATUS
FROM PRODUCT P
LEFT JOIN CATEGORY C ON P.CATEGORY_ID = C.CATEGORY_ID
LEFT JOIN SUPPLIER S ON P.SUPPLIER_ID = S.SUPPLIER_ID
LEFT JOIN INVENTORY I ON P.PRODUCT_ID = I.PRODUCT_ID
LEFT JOIN WAREHOUSE W ON I.WAREHOUSE_ID = W.WAREHOUSE_ID
LEFT JOIN LOCATION L ON I.LOCATION_ID = L.LOCATION_ID
WHERE P.IS_ACTIVE = TRUE;

CREATE VIEW V_SALES_ORDER_SUMMARY AS
SELECT 
    SO.SALES_ORDER_ID,
    SO.ORDER_NUMBER,
    SO.ORDER_DATE,
    C.CUSTOMER_NAME,
    SO.ORDER_STATUS,
    SO.SUBTOTAL,
    SO.TAX_AMOUNT,
    SO.SHIPPING_COST,
    SO.TOTAL_AMOUNT,
    COUNT(SOL.LINE_ID) AS LINE_COUNT,
    SUM(SOL.QUANTITY_ORDERED) AS TOTAL_ITEMS_ORDERED,
    SUM(SOL.QUANTITY_SHIPPED) AS TOTAL_ITEMS_SHIPPED
FROM SALES_ORDER SO
JOIN CUSTOMER C ON SO.CUSTOMER_ID = C.CUSTOMER_ID
LEFT JOIN SALES_ORDER_LINE SOL ON SO.SALES_ORDER_ID = SOL.SALES_ORDER_ID
GROUP BY SO.SALES_ORDER_ID, SO.ORDER_NUMBER, SO.ORDER_DATE, C.CUSTOMER_NAME, 
         SO.ORDER_STATUS, SO.SUBTOTAL, SO.TAX_AMOUNT, SO.SHIPPING_COST, SO.TOTAL_AMOUNT;

CREATE VIEW V_PURCHASE_ORDER_STATUS AS
SELECT 
    PO.PURCHASE_ORDER_ID,
    PO.PO_NUMBER,
    PO.ORDER_DATE,
    S.SUPPLIER_NAME,
    W.WAREHOUSE_NAME,
    PO.PO_STATUS,
    PO.TOTAL_AMOUNT,
    COUNT(POL.PO_LINE_ID) AS LINE_COUNT,
    SUM(POL.QUANTITY_ORDERED) AS TOTAL_ITEMS_ORDERED,
    SUM(POL.QUANTITY_RECEIVED) AS TOTAL_ITEMS_RECEIVED
FROM PURCHASE_ORDER PO
JOIN SUPPLIER S ON PO.SUPPLIER_ID = S.SUPPLIER_ID
JOIN WAREHOUSE W ON PO.WAREHOUSE_ID = W.WAREHOUSE_ID
LEFT JOIN PURCHASE_ORDER_LINE POL ON PO.PURCHASE_ORDER_ID = POL.PURCHASE_ORDER_ID
GROUP BY PO.PURCHASE_ORDER_ID, PO.PO_NUMBER, PO.ORDER_DATE, S.SUPPLIER_NAME, 
         W.WAREHOUSE_NAME, PO.PO_STATUS, PO.TOTAL_AMOUNT;

